package com.user.bean;

public class wishbean {
	
	private String wish_image;
	private String wish_name;
	private int wish_price;
	private int wish_qty;
	private String wish_content;
	
	
	public String getWish_image() {
		return wish_image;
	}
	public void setWish_image(String wish_image) {
		this.wish_image = wish_image;
	}
	public String getWish_name() {
		return wish_name;
	}
	public void setWish_name(String wish_name) {
		this.wish_name = wish_name;
	}
	public int getWish_price() {
		return wish_price;
	}
	public void setWish_price(int wish_price) {
		this.wish_price = wish_price;
	}
	public int getWish_qty() {
		return wish_qty;
	}
	public void setWish_qty(int wish_qty) {
		this.wish_qty = wish_qty;
	}
	public String getWish_content() {
		return wish_content;
	}
	public void setWish_content(String wish_content) {
		this.wish_content = wish_content;
	}
	
	
	
	
	
}
